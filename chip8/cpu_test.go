package chip8

import (
	"testing"
)

func TestGetPrettyCpuState(t *testing.T) {
	tests := map[string]struct {
		memoryPokes map[uint16]uint8
		cpuState    Cpu
		wantErr     bool
	}{
		"default": {
			cpuState: Cpu{
				V: [0x10]uint8{
					0x12, 0x34, 0x56, 0x78, 0x9A, 0xBC, 0xDE, 0xF0, 0x0F, 0xED, 0xCB, 0xA9, 0x87, 0x65, 0x43, 0x21,
				},
				Stack: [0x10]uint8{
					0x12, 0x34, 0x56, 0x78, 0x9A, 0xBC, 0xDE, 0xF0, 0x0F, 0xED, 0xCB, 0xA9, 0x87, 0x65, 0x43, 0x21,
				},
				I:  0x12,
				PC: 0x34,
				SP: 0x56,
				DT: 0x78,
				ST: 0x9A,
			},
			wantErr: false,
		},
	}

	for name, test := range tests {
		t.Run(name, func(t *testing.T) {
			output := test.cpuState.GetPrettyCpuState()
			t.Log(output)
		})
	}
}
